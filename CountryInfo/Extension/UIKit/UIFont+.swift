import UIKit

extension UIFont {
  
  static func helveticaNeue20() -> UIFont? {
      return UIFont.init(name: "Helvetica Neue", size: 18)
  }
  
  static func helveticaNeue26() -> UIFont? {
      return UIFont.init(name: "Helvetica Neue", size: 25)
  }
}
